package in.co.operators;

public class Relation {
    public static void main(String[] args) {
         //  **Equal Operator **
        int a =10;
        int b =15;
        System.out.println(a==b);
        System.out.println("\n");
        // Not equal to
        System.out.println(a!=b);

        // Greater to
        System.out.println(a>b);
        System.out.println("\n");
        //Less than
        System.out.println(a<b);

    }
}
